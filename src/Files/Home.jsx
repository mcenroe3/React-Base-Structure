import Main from "./Main";
import Basket from "./Basket";
import data from "./data";
import { useState } from "react";

import { useNavigate } from "react-router-dom";

function Home() {
  let history = useNavigate();
  const { products } = data;
  const [cartItems, setCartItems] = useState([]);
  const onAdd = (product) => {
    const exist = cartItems.find((x) => x.id === product.id);
    if (exist) {
      setCartItems(
        cartItems.map((x) =>
          x.id === product.id ? { ...exist, qty: exist.qty + 1 } : x
        )
      );
    } else {
      setCartItems([...cartItems, { ...product, qty: 1 }]);
    }
  };
  const onRemove = (product) => {
    const exist = cartItems.find((x) => x.id === product.id);
    if (exist.qty === 1) {
      setCartItems(cartItems.filter((x) => x.id !== product.id));
    } else {
      setCartItems(
        cartItems.map((x) =>
          x.id === product.id ? { ...exist, qty: exist.qty - 1 } : x
        )
      );
    }
  };
  return (
    <div className="App">
      <div>
        <button className="Button1" onClick={() => history("/wishlist")}>
          WISHLIST
        </button>
        <button className="Button1" onClick={() => history("/customers")}>
          CUSTOMERS
        </button>
        <button className="Button2" onClick={() => history("/customercare")}>
          CUSTOMER CARE
        </button>
      </div>
      <div className="row">
        <Main products={products} onAdd={onAdd}></Main>
        <Basket
          cartItems={cartItems}
          onAdd={onAdd}
          onRemove={onRemove}
        ></Basket>
      </div>
    </div>
  );
}

export default Home;
